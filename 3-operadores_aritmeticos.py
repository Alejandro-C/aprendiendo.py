numeroA = 19
numeroB = 16
numeroC = 2
numeroD = 3

# Suma
resultado = numeroA + numeroB
print(numeroA, '+', numeroB, '=', resultado)

# Resta
resultado = numeroA - numeroB
print(numeroA, '-', numeroB, '=', resultado)

# Multiplicacion
resultado = numeroA * numeroB
print(numeroA, 'x', numeroB, '=', resultado)

# Exponenete
resultado = numeroC ** numeroD
print(numeroC, '^', numeroD, '=', resultado)

# Divicion
resultado = numeroA / numeroB
print(numeroA, '/', numeroB, '=', resultado)

# Divicion Entera
resultado = numeroA // numeroB
print(numeroA, '//', numeroB, '=', resultado)

# Modulo
resultado = numeroA % numeroB
print(numeroA, '%', numeroB, '=', resultado)