''' OPERADORES RELACIONALES '''
# ==    Igual que        
# !=    Distinto que     
# <     Menor que        
# >     Mayor que        
# <=    Menor o igual que
# >=    Mayor o igual que

print(5 == 7)  # Fase
print('rojo' != 'verde')  # True
print(8 < 12)  # True
print(12 > 7)  # True
print(12 <= 12)  # True
print(4 >= 5)  # False


''' OPERADORES LOGICOS '''
# and   y
# or    o 

print(5 == 7 and 7 < 12)  # False
print(12 == 12 or 15 < 7)  # True